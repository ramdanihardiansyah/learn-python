# 4 Tipe data dasar di python
# Integer : Bilangan Bulat
# Float : Bilangan Desimal
# Boolean : True / False
# String : Teks
# untuk string multiline maka menggunakan """ diawal dan diakhir dan bisa juga menggunakan '''

umur = 23
tinggi = 177.5
nama = "Ramdani"
is_student = True

print("Tipe data umur adalah", type(umur))           # Output: <class 'int'>
print("Tipe data tinggi adalah", type(tinggi))       # Output: <class 'float'>
print("Tipe data nama adalah", type(nama))           # Output: <class 'str'>
print("Tipe data is_student adalah", type(is_student)) # Output: <class 'bool'>